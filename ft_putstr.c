#include "ft_printf.h"

int	ft_putstr(char *str, int *index)
{
	int	i;

	(*index) += 2;
	if (!str)
		return (write(1, "(null)", 6));
	i = 0;
	while (str[i])
		write(1, &str[i++], 1);
	return (i);
}
